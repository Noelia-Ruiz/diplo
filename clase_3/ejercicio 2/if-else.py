receta = "chocotorta"  # Variable con un valor asignado

if receta == "tiramisú":
    print("La receta es tiramisú.")
elif receta == "chocotorta":
    print("La receta es chocotorta.")
else:
    print("La receta no es ni tiramisú ni chocotorta.")
    